void	ft_sort_int_tab(int *tab, int size)
{
	int	i;
	int	j;
	int	k;

	k = 0;
	i = 0;
	j = 1;
	while (i < size - 1)
	{
		while (j <= size - 1)
		{
			if (tab[j] < tab[i])
			{
				k = tab[i];
				tab[i] = tab[j];
				tab[j] = k;
			}
			j++;
		}
		j = i + 1;
		i++;
	}
}
